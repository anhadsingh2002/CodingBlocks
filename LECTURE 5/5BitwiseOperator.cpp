//BITWISE
#include <iostream>
using namespace std;

int main() {

    cout << endl;
    cout << "5&7: " << (5 & 7) << endl;               //AND
    cout << "500&700: " << (500 & 700) << endl;
    cout << endl;

    cout << "5|7: " << (5 | 7) << endl;               //OR
    cout << endl;


    cout << "5^7: " << (5 ^ 7) << endl;               //XOR
    cout << endl;

    return 0;
}
